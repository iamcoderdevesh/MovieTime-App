package com.example.home;

public class TheatreRecycler {
    String tName, language, screen, showid, time, tId, date;

    public TheatreRecycler(String tName, String language, String screen, String showid, String time, String tId, String date){
        this.tName = tName;
        this.language = language;
        this.screen = screen;
        this.showid = showid;
        this.time = time;
        this.tId = tId;
        this.date = date;
    }
}
